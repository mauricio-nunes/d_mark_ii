-------------- CONFIGURACAO EMPRESAS ------------
CREATE TABLE IF NOT EXISTS empresa (
  id INTEGER PRIMARY KEY AUTOINCREMENT,
  cnpj TEXT UNIQUE NOT NULL,
  codigo_negociacao TEXT UNIQUE NOT NULL,
  classificacao TEXT NULL,
  tipo_dfp TEXT NULL CHECK (tipo_dfp IN ('individual', 'consolidado')),
  modo_analise TEXT NULL CHECK (modo_analise IN ('padrao', 'banco', 'seguradora')),
  criado_em TEXT NOT NULL DEFAULT (datetime('now')),
  atualizado_em TEXT NOT NULL DEFAULT (datetime('now')),
  UNIQUE(cnpj,codigo_negociacao)
);

-- Trigger para atualizar timestamp de atualização
CREATE TRIGGER IF NOT EXISTS update_empresa_timestamp 
    AFTER UPDATE ON empresa
BEGIN
    UPDATE empresa SET atualizado_em = datetime('now') WHERE id = NEW.id;
END;

